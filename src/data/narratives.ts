// ============================================================
// Helper: 根据维度和水平获取模板
// ============================================================

export function getTemplate(
  dimension: Domain,
  level: ScoreLevel,
): NarrativeTemplate | undefined {
  return NARRATIVE_TEMPLATES.find(
    (t) => t.dimension === dimension && t.level === level,
  );
}

// ============================================================
// Helper: 根据维度和子维度获取描述
// ============================================================

export function getFacetNarrative(
  domain: Domain,
  facet: string,
): FacetNarrative | undefined {
  return FACET_NARRATIVES.find(
    (f) => f.domain === domain && f.facet === facet,
  );
}
