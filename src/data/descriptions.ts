/** 自我价值感 0-100 */
export function interpretSelfEsteem(score: number): string {
  if (score >= 80) return '你对自己整体上是认可的，能看到自己的价值，也接受自己的不完美。这种对自己的善意是很重要的内在力量。';
  if (score >= 60) return '大多数时候你对自己感觉还可以，但偶尔也会怀疑自己值不值得。这种波动很正常，关键是对自己别太苛刻。';
  if (score >= 40) return '你有时候会觉得自己不够好——也许是对自己的要求太高了。试着像对待朋友一样对待自己，你可能比自己想象中要好。';
  return '你近来可能对自己比较严厉，常常觉得自己比不上别人。这不是你一个人的感觉，很多人都会有这样的时刻。试着从小的肯定开始，比如记录每天做到的一件事。';
}

/** 关系焦虑 0-100 */
export function interpretAttachmentAnxiety(score: number): string {
  if (score >= 70) return '你在关系里比较敏感，容易担心别人是不是真的在意你。这种不安可能让你在关系中小心翼翼，或者反复求证。';
  if (score >= 45) return '你有时会担心自己在关系中的位置，不过大多数时候还能保持安心。一段让你感到安全的关系，能帮你放松很多。';
  return '你在关系中比较有安全感，不会过分担心别人的看法或态度。这种放松的状态让你更容易建立稳定的关系。';
}

/** 关系回避 0-100 */
export function interpretAttachmentAvoidance(score: number): string {
  if (score >= 70) return '你习惯保持距离，不太愿意让别人靠得太近。独立当然好，但如果发现自己其实想要亲近却又推开别人，可能就是一种习惯了。';
  if (score >= 45) return '你在独立和亲密之间寻找平衡——既需要自己的空间，也不排斥亲近。这是很多人都在摸索的位置。';
  return '你享受亲密关系，愿意信任和依赖他人。这种开放的态度是建立深厚关系的基础。';
}

/** 抑郁筛查（PHQ-8 原始分 0-24）— 措辞柔和，不贴诊断标签 */
export function interpretDepression(score: number): string {
  if (score <= 4) return '最近这段时间你的情绪状态看起来还不错，没有明显的低落或疲惫感。继续保持对自己的关注就好。';
  if (score <= 9) return '最近你可能有些情绪低落或者觉得累，不过还在可以调整的范围内。试着多照顾一下自己——休息、找人聊聊、或者做些让自己放松的事。';
  if (score <= 14) return '这段时间你好像过得不太轻松，心里装着不少事。如果这种状态已经持续了一段时间，建议找信任的朋友聊聊，或者去和医生谈一谈。不需要一个人扛着。';
  return '最近你身上的负担可能比较重，情绪和身体都发出了信号。这不是小事，如果这种状态持续了超过两周，很建议去找医生聊聊——不一定是大事，但有人帮你看看心里会更踏实。';
}

/** 焦虑筛查（GAD-7 原始分 0-21） */
export function interpretAnxiety(score: number): string {
  if (score <= 4) return '你最近的焦虑水平不高，整体比较放松，没有明显的紧张或担忧。';
  if (score <= 9) return '你最近可能有些焦虑——心里不踏实、容易多想，不过还在可控范围内。试着给自己一些放松的时间，比如深呼吸、散步，或者暂时放下手机。';
  if (score <= 14) return '最近你的焦虑感比较明显，脑子停不下来、身体也紧绷着。这种状态很难受，如果已经持续了一段时间，建议找医生聊聊，学习一些缓解的方法。';
  return '你最近的焦虑水平比较高，可能已经影响到了日常生活。这不是你"想太多"的问题，而是身体和心理在告诉你需要帮助。去找医生聊一聊，比自己硬扛有效得多。';
}

/** 睡眠质量 0-100 */
export function interpretSleep(score: number): string {
  if (score >= 80) return '你的睡眠状态不错，作息也比较规律，这是很好的基础。';
  if (score >= 60) return '睡眠质量还可以，但有时候睡得不太好或者作息不太固定。偶尔调整一下也没关系。';
  if (score >= 40) return '你的睡眠有些问题，睡得不够好或者作息比较乱。这可能会影响白天的状态，试着从固定上床时间开始调整。';
  return '睡眠质量看起来不太好，这可能是影响你状态的一个重要因素。如果长期睡不好，建议找医生看看，睡眠问题很多时候是可以改善的。';
}
