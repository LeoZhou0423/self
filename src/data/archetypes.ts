// ── Matching Logic ─────────────────────────────────────────────

/** Score record keyed by domain letter (O, C, E, A, N), each 0-100. */
export type ScoreRecord = Record<Domain, number>;

/**
 * Compute how well `scores` satisfy an archetype's `pattern`.
 * Returns 0-1 where 1 = perfectly matched.
 */
function matchPattern(scores: ScoreRecord, pattern: ArchetypePattern): number {
  let total = 0;
  let constrained = 0;

  for (const dim of DIMS) {
    const level = pattern[dim];
    if (level == null) continue;
    constrained++;

    const score = scores[dim];

    if (level === 'high') {
      // Ramp from 0 at score <= 30 to 1 at score >= 60
      if (score >= 60) total += 1;
      else if (score <= 30) total += 0;
      else total += (score - 30) / 30;
    } else if (level === 'low') {
      // Ramp from 1 at score <= 40 to 0 at score >= 70
      if (score <= 40) total += 1;
      else if (score >= 70) total += 0;
      else total += (70 - score) / 30;
    } else {
      // 'middle': peak at 50, taper to 0 at 0 and 100
      if (score >= 40 && score <= 60) {
        total += 1;
      } else if (score < 40) {
        total += Math.max(0, score / 40);
      } else {
        total += Math.max(0, (100 - score) / 40);
      }
    }
  }

  return constrained > 0 ? total / constrained : 0.5;
}

/**
 * Find the best-matching archetype for a set of Big Five domain scores.
 *
 * Ties are broken in favor of the archetype with **more** pattern constraints
 * (more specific match), then by array order (earlier in ARCHETYPES wins).
 */
export function matchArchetype(scores: ScoreRecord): Archetype {
  let best = ARCHETYPES[0];
  let bestScore = -1;
  let bestConstraints = -1;

  for (const arch of ARCHETYPES) {
    const score = matchPattern(scores, arch.pattern);
    const constraintCount = Object.keys(arch.pattern).length;

    if (
      score > bestScore ||
      (score === bestScore && constraintCount > bestConstraints)
    ) {
      bestScore = score;
      bestConstraints = constraintCount;
      best = arch;
    }
  }

  return best;
}
