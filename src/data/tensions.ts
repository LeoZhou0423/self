import type { Domain } from './questions';

/**
 * 维度对：两个大五人格维度构成的张力轴。
 * 通常是两个维度同时偏高时产生内部冲突，
 * 个别张力也涵盖一个偏高一个偏低的情况（如低宜人性+高外向性）。
 */
export type DimensionPair = readonly [Domain, Domain];

/**
 * 张力模式：描述两个大五维度同时偏高（或特定高低组合）时
 * 产生的内部心理冲突及其应对方式。
 */
export interface TensionPattern {
  /** 唯一标识，格式: 维度A_维度B */
  id: string;
  /** 中文名称 */
  name: string;
  /** 产生张力的两个维度（无序对） */
  dimensions: DimensionPair;
  /**
   * 行为表现：日常生活中的具体行为模式。
   * 包含可观察的外在表现和可验证的情境，
   * 帮助用户在看到描述时产生"这就是我"的共鸣。
   */
  description: string;
  /**
   * 内在体验：这种张力在内心如何被感受。
   * 聚焦情绪层面的体验，让用户理解
   * 自己内心矛盾的情感根源。
   */
  innerExperience: string;
  /**
   * 成长策略：具体、可操作的应对方法。
   * 每条策略必须有行为层面的可执行步骤，
   * 而不是抽象的"找到平衡"式建议。
   */
  growthStrategy: string;
}

